package chapter4.ifexample;

public class IfExample1 {
    public static void main(String[] args) {
        int age = 7;
        // if~else문 : 주어진 조건문에 참과 거짓을 판별하여 각 문장을 출력함
        if(age >= 8){
            System.out.println("학교에 다닙니다.");
        }else{
            System.out.println("학교에 다니지 않습니다.");
        }
    }
}
